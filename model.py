import torch.nn as nn
import torch.nn.functional as F


class LeNet(nn.Module):
    def __init__(self,num_classes=1000):  #num_classes
        super(LeNet, self).__init__()
        self.conv1 = nn.Conv2d(3, 16, 5)
        self.pool1 = nn.MaxPool2d(2, 2)
        #self.conv2 = nn.Conv2d(16, 32, 5)
        self.conv2 = nn.Conv2d(16, 32, kernel_size=5, padding=1)
        self.pool2 = nn.MaxPool2d(2, 2)
        self.fc1 = nn.Linear(32*54*54, 120)          ##################展平的图片尺寸需自己计算
        self.fc2 = nn.Linear(120, 84)
        self.fc3 = nn.Linear(84, num_classes)

    def forward(self, x):
        x = F.relu(self.conv1(x))    # input(3, 32, 32) output(16, 220, 220)
        x = self.pool1(x)            # output(16, 110, 110)
        x = F.relu(self.conv2(x))    # output(32, 108, 108)
        x = self.pool2(x)            # output(32, 54, 54)
        x = x.view(-1, 32*54*54)       # output(32*54*54)
        x = F.relu(self.fc1(x))      # output(120)
        x = F.relu(self.fc2(x))      # output(84)
        x = self.fc3(x)              # output(10)
        return x



